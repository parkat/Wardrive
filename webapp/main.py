@app.get("/api/analytics")
async def get_analytics():
    """Return aggregated analytics data for dashboard visualization."""
    if not DB_PATH.exists():
        return {"error": "Database not found"}

    try:
        with sqlite3.connect(str(DB_PATH)) as db:
            db.row_factory = sqlite3.Row

            # Signal strength distributions (per table - faster than UNION)
            ble_signal = db.execute("""
                SELECT CASE WHEN max_rssi_dbm >= -50 THEN 'Strong (-50 to 0)' WHEN max_rssi_dbm >= -70 THEN 'Good (-70 to -50)' WHEN max_rssi_dbm >= -85 THEN 'Fair (-85 to -70)' ELSE 'Weak (< -85)' END as signal_range, COUNT(*) as count
                FROM bt_devices WHERE max_rssi_dbm IS NOT NULL GROUP BY signal_range ORDER BY signal_range
            """).fetchall()

            wifi_signal = db.execute("""
                SELECT CASE WHEN max_signal_dbm >= -50 THEN 'Strong (-50 to 0)' WHEN max_signal_dbm >= -70 THEN 'Good (-70 to -50)' WHEN max_signal_dbm >= -85 THEN 'Fair (-85 to -70)' ELSE 'Weak (< -85)' END as signal_range, COUNT(*) as count
                FROM wifi_aps WHERE max_signal_dbm IS NOT NULL GROUP BY signal_range ORDER BY signal_range
            """).fetchall()

            # Device type counts
            device_types = db.execute("""
                SELECT 'BLE' as device_type, COUNT(*) as count FROM bt_devices
                UNION ALL SELECT 'WiFi AP', COUNT(*) FROM wifi_aps
                UNION ALL SELECT 'WiFi Client', COUNT(*) FROM wifi_clients
                UNION ALL SELECT 'RF Device', COUNT(*) FROM rf_devices
            """).fetchall()

            # Top manufacturers (BLE only for speed)
            top_manufacturers = db.execute("""
                SELECT manufacturer, COUNT(*) as count FROM bt_devices
                WHERE manufacturer IS NOT NULL GROUP BY manufacturer ORDER BY count DESC LIMIT 10
            """).fetchall()

            # WiFi encryption distribution
            wifi_encryption = db.execute("""
                SELECT encryption, COUNT(*) as count FROM wifi_aps
                WHERE encryption IS NOT NULL GROUP BY encryption ORDER BY count DESC LIMIT 10
            """).fetchall()

            # Hourly discovery (fast version - aggregate by hour from each table)
            hourly_dict = {}

            # BLE observations by hour
            for row in db.execute("SELECT strftime('%Y-%m-%d %H:00:00', timestamp_utc) as hour, COUNT(DISTINCT address) as cnt FROM bt_obs GROUP BY hour"):
                if row["hour"] not in hourly_dict:
                    hourly_dict[row["hour"]] = {"hour": row["hour"], "ble": 0, "wifi_ap": 0, "rf": 0}
                hourly_dict[row["hour"]]["ble"] = row["cnt"] or 0

            # WiFi observations by hour
            for row in db.execute("SELECT strftime('%Y-%m-%d %H:00:00', timestamp_utc) as hour, COUNT(DISTINCT bssid) as cnt FROM wifi_obs WHERE bssid IS NOT NULL GROUP BY hour"):
                if row["hour"] not in hourly_dict:
                    hourly_dict[row["hour"]] = {"hour": row["hour"], "ble": 0, "wifi_ap": 0, "rf": 0}
                hourly_dict[row["hour"]]["wifi_ap"] = row["cnt"] or 0

            # RF observations by hour
            for row in db.execute("SELECT strftime('%Y-%m-%d %H:00:00', timestamp_utc) as hour, COUNT(DISTINCT device_id) as cnt FROM rf_obs GROUP BY hour"):
                if row["hour"] not in hourly_dict:
                    hourly_dict[row["hour"]] = {"hour": row["hour"], "ble": 0, "wifi_ap": 0, "rf": 0}
                hourly_dict[row["hour"]]["rf"] = row["cnt"] or 0

            hourly = sorted(hourly_dict.values(), key=lambda x: x["hour"])

            # Session comparison stats - counts per session
            session_ble = [row["ble_count"] for row in db.execute("""
                SELECT COUNT(DISTINCT address) as ble_count FROM bt_obs GROUP BY session_id ORDER BY session_id DESC
            """).fetchall()]

            session_wifi = [row["wifi_count"] for row in db.execute("""
                SELECT COUNT(DISTINCT bssid) as wifi_count FROM wifi_obs WHERE bssid IS NOT NULL GROUP BY session_id ORDER BY session_id DESC
            """).fetchall()]

            avg_ble = sum(session_ble) / len(session_ble) if session_ble else 0
            avg_wifi = sum(session_wifi) / len(session_wifi) if session_wifi else 0

            return {
                "signal_strength": {
                    "ble": [{"range": row["signal_range"], "count": row["count"]} for row in ble_signal],
                    "wifi": [{"range": row["signal_range"], "count": row["count"]} for row in wifi_signal]
                },
                "device_types": [{"type": row["device_type"], "count": row["count"]} for row in device_types],
                "top_manufacturers": [{"manufacturer": row["manufacturer"], "count": row["count"]} for row in top_manufacturers],
                "wifi_encryption": [{"encryption": row["encryption"], "count": row["count"]} for row in wifi_encryption],
                "hourly_discovery": [{"hour": row["hour"], "ble": row["ble"] or 0, "wifi_ap": row["wifi_ap"] or 0, "rf": row["rf"] or 0} for row in hourly],
                "session_comparison": {
                    "ble_per_session": session_ble,
                    "wifi_per_session": session_wifi,
                    "avg_ble": round(avg_ble, 1),
                    "avg_wifi": round(avg_wifi, 1)
                }
            }
    except Exception as e:
        logging.error(f"Analytics error: {e}")
        return {"error": str(e)}

@app.get("/api/storage/stats")
async def get_storage_stats():
    """Return disk usage for the database, per-session capture dirs, and the underlying filesystem."""
    capture_raw = _get_capture_raw_dir()

    result = {
        "db": {"path": str(DB_PATH), "size_bytes": 0, "exists": DB_PATH.exists()},
        "capture_dir": str(capture_raw),
        "capture_base_dir": _get_config_value("CAPTURE_BASE_DIR") or "",
        "total_capture_bytes": 0,
        "sessions": [],
        "disk": None,
    }

    if DB_PATH.exists():
        try:
            result["db"]["size_bytes"] = DB_PATH.stat().st_size
        except OSError:
            pass

    # Pull session DB counts in one query
    db_sessions: dict = {}
    if DB_PATH.exists():
        try:
            with sqlite3.connect(str(DB_PATH)) as db:
                db.row_factory = sqlite3.Row
                for row in db.execute("""
                    SELECT s.session_id, s.started_at_utc, s.ended_at_utc,
                        (SELECT COUNT(DISTINCT bssid)     FROM wifi_obs WHERE session_id = s.session_id) AS ap_count,
                        (SELECT COUNT(DISTINCT address)   FROM bt_obs   WHERE session_id = s.session_id) AS bt_count,
                        (SELECT COUNT(DISTINCT device_id) FROM rf_obs   WHERE session_id = s.session_id) AS rf_count
                    FROM sessions s ORDER BY s.started_at_utc DESC
                """).fetchall():
                    r = dict(row)
                    db_sessions[r["session_id"]] = r
        except Exception as e:
            logging.error(f"Storage stats DB error: {e}")

    sessions = []
    total_capture_bytes = 0

    # Sessions that have raw files on disk
    raw_ids: set = set()
    if capture_raw.exists():
        for d in sorted(capture_raw.iterdir(), reverse=True):
            if not d.is_dir():
                continue
            raw_ids.add(d.name)
            size = _dir_size_bytes(d)
            total_capture_bytes += size
            db_info = db_sessions.get(d.name, {})
            ap = db_info.get("ap_count") or 0
            bt = db_info.get("bt_count") or 0
            rf = db_info.get("rf_count") or 0
            started = db_info.get("started_at_utc")
            ended = db_info.get("ended_at_utc")
            duration_sec = None
            if started and ended:
                try:
                    s0 = datetime.fromisoformat(started.replace("Z", "+00:00"))
                    s1 = datetime.fromisoformat(ended.replace("Z", "+00:00"))
                    duration_sec = int((s1 - s0).total_seconds())
                except Exception:
                    pass
            sessions.append({
                "session_id": d.name,
                "size_bytes": size,
                "has_raw": True,
                "in_db": d.name in db_sessions,
                "started_at_utc": started,
                "ended_at_utc": ended,
                "ap_count": ap,
                "bt_count": bt,
                "rf_count": rf,
                "total_devices": ap + bt + rf,
                "duration_sec": duration_sec,
                "is_active": bool(started and not ended),
            })

    # DB-only sessions (raw files already gone)
    for sid, db_info in db_sessions.items():
        if sid in raw_ids:
            continue
        ap = db_info.get("ap_count") or 0
        bt = db_info.get("bt_count") or 0
        rf = db_info.get("rf_count") or 0
        started = db_info.get("started_at_utc")
        ended = db_info.get("ended_at_utc")
        duration_sec = None
        if started and ended:
            try:
                s0 = datetime.fromisoformat(started.replace("Z", "+00:00"))
                s1 = datetime.fromisoformat(ended.replace("Z", "+00:00"))
                duration_sec = int((s1 - s0).total_seconds())
            except Exception:
                pass
        sessions.append({
            "session_id": sid,
            "size_bytes": 0,
            "has_raw": False,
            "in_db": True,
            "started_at_utc": started,
            "ended_at_utc": ended,
            "ap_count": ap,
            "bt_count": bt,
            "rf_count": rf,
            "total_devices": ap + bt + rf,
            "duration_sec": duration_sec,
            "is_active": bool(started and not ended),
        })

    # Sort combined list by started_at_utc descending
    sessions.sort(key=lambda x: x.get("started_at_utc") or "", reverse=True)
    result["sessions"] = sessions
    result["total_capture_bytes"] = total_capture_bytes

    # Filesystem stats for the capture mount (or project root as fallback)
    check_path = capture_raw if capture_raw.exists() else PROJECT_ROOT
    try:
        usage = shutil.disk_usage(str(check_path))
        result["disk"] = {
            "total_bytes": usage.total,
            "used_bytes": usage.used,
            "free_bytes": usage.free,
        }
    except Exception:
        pass

    return result


@app.delete("/api/sessions/{session_id}")
async def delete_session(session_id: str):
    """Delete a session's raw files and DB records. Refuses to delete an active session."""
    _validate_session(session_id)

    capture_raw = _get_capture_raw_dir()
    session_dir = (capture_raw / session_id).resolve()

    # Path traversal guard
    if not str(session_dir).startswith(str(capture_raw.resolve())):
        raise HTTPException(status_code=400, detail="Invalid session path")

    # Refuse to delete an active (still-running) session
    manifest_path = session_dir / "manifest.json"
    if manifest_path.exists():
        try:
            with open(manifest_path) as f:
                manifest = json.load(f)
            if not manifest.get("ended_at_utc"):
                raise HTTPException(status_code=409, detail="Cannot delete an active session")
        except HTTPException:
            raise
        except Exception:
            pass

    deleted = {"db_records": False, "files": False, "errors": []}

    # Remove observation and session rows from the database
    if DB_PATH.exists():
        try:
            with sqlite3.connect(str(DB_PATH)) as db:
                db.execute("DELETE FROM bt_obs   WHERE session_id = ?", (session_id,))
                db.execute("DELETE FROM wifi_obs  WHERE session_id = ?", (session_id,))
                db.execute("DELETE FROM rf_obs    WHERE session_id = ?", (session_id,))
                db.execute("DELETE FROM sessions  WHERE session_id = ?", (session_id,))
                db.commit()
            deleted["db_records"] = True
        except Exception as e:
            deleted["errors"].append(f"DB error: {e}")

    # Remove raw capture directory
    if session_dir.exists():
        try:
            shutil.rmtree(str(session_dir))
            deleted["files"] = True
        except Exception as e:
            deleted["errors"].append(f"File error: {e}")

    status = "partial" if deleted["errors"] else "ok"
    return {"status": status, "session_id": session_id, "deleted": deleted}


@app.post("/api/storage/config")
async def update_storage_config(request: Request):
    """Write CAPTURE_BASE_DIR to wardrive.conf."""
    body = await request.json()
    new_path = str(body.get("capture_base_dir", "")).strip()

    if new_path:
        if not Path(new_path).is_absolute():
            raise HTTPException(status_code=400, detail="Path must be absolute")
        try:
            (Path(new_path) / "raw").mkdir(parents=True, exist_ok=True)
        except Exception as e:
            raise HTTPException(status_code=400, detail=f"Cannot access directory: {e}")

    if not CONFIG_PATH.exists():
        raise HTTPException(status_code=500, detail="Config file not found")

    try:
        config_text = CONFIG_PATH.read_text()
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Cannot read config: {e}")

    new_line = f'CAPTURE_BASE_DIR="{new_path}"'
    if re.search(r'^CAPTURE_BASE_DIR=', config_text, re.MULTILINE):
        config_text = re.sub(r'^CAPTURE_BASE_DIR=.*$', new_line, config_text, flags=re.MULTILINE)
    else:
        config_text += f'\n{new_line}\n'

    try:
        CONFIG_PATH.write_text(config_text)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Cannot write config: {e}")

    return {"status": "ok", "capture_base_dir": new_path}


