# ── Session name validation ───────────────────────────────────────────────────
# Session IDs are generated by wardrive.sh as <TIMESTAMP>_<LABEL> and contain
# only alphanumeric characters, underscores, and hyphens.
_SESSION_RE = re.compile(r'^[A-Za-z0-9_\-]{1,128}$')

def _validate_session(session: str | None) -> str | None:
    """Return session if it is a valid session identifier, else raise HTTPException."""
    if session is None or session == "":
        return None
    if not _SESSION_RE.match(session):
        raise HTTPException(status_code=400, detail="Invalid session identifier")
    return session

def _get_config_value(key: str) -> str | None:
    """Read a single variable from wardrive.conf. Returns None if absent or blank."""
    if not CONFIG_PATH.exists():
        return None
    try:
        with open(CONFIG_PATH) as f:
            for line in f:
                line = line.strip()
                if line.startswith(f"{key}="):
                    val = line[len(key) + 1:].strip('"').strip("'")
                    return val if val else None
    except Exception:
        return None
    return None

def _get_capture_raw_dir() -> Path:
    """Return the configured capture/raw directory, falling back to project default."""
    base = _get_config_value("CAPTURE_BASE_DIR")
    if base:
        return Path(base) / "raw"
    return PROJECT_ROOT / "capture" / "raw"

def _dir_size_bytes(path: Path) -> int:
    """Return total byte size of all files under path."""
    total = 0
    try:
        for p in path.rglob("*"):
            if p.is_file():
                try:
                    total += p.stat().st_size
                except OSError:
                    pass
    except Exception:
        pass
    return total

