def _get_ble_gps_positions(session_id=None):
    capture_raw = _get_capture_raw_dir()
    best = {}
    if not capture_raw.exists():
        return best
    if session_id:
        # Resolve the path and ensure it stays inside capture/raw (path traversal guard)
        target = (capture_raw / session_id).resolve()
        if not str(target).startswith(str(capture_raw.resolve())):
            return best
        dirs = [target]
    else:
        dirs = sorted(capture_raw.iterdir(), reverse=True)
    for d in dirs:
        if not (isinstance(d, Path) and d.is_dir()):
            continue
        ndjson = d / "bt" / "esp32_ble.ndjson"
        if not ndjson.exists():
            continue
        with open(ndjson) as f:
            for line in f:
                try:
                    obj = json.loads(line)
                    addr = obj.get("addr")
                    if not addr or "lat" not in obj or "lon" not in obj:
                        continue
                    rssi = obj.get("rssi", -100)
                    if addr not in best or rssi > best[addr]["rssi_dbm"]:
                        best[addr] = {
                            "lat": obj["lat"], "lon": obj["lon"],
                            "rssi_dbm": rssi,
                            "name": obj.get("name") or obj.get("local_name"),
                        }
                except Exception:
                    pass
    return best


